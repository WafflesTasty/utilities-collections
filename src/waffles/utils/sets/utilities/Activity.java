package waffles.utils.sets.utilities;

/**
 * The {@code Activity} enum defines modes of action.
 *
 * @author Waffles
 * @since 11 May 2024
 * @version 1.1
 */
public enum Activity
{
	/**
	 * An active object is used in logic.
	 */
	ACTIVE,
	/**
	 * An idle object remains unused.
	 */
	IDLE;
}